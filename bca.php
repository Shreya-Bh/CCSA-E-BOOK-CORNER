                function handleDownload(fileURL, redirectPage) {
                    
                    var a = document.createElement('a');
                    a.href = fileURL;
                    a.target = '_blank'; 
                    a.download = 'downloaded_file.pdf';
        
                    
                    a.click();
        
                    
